package obj;

public class Carro {
	public String placa, chassi, modelo, ano, cor, nomeDono;
	public boolean multado;
	
	public Carro(String placa, String chassi, String modelo, String ano, String cor, String nomeDono, boolean multado) {
		this.placa = placa;
		this.chassi = chassi;
		this.modelo = modelo;
		this.ano = ano;
		this.cor = cor;
		this.nomeDono = nomeDono;
		this.multado = multado;
	}
	
}
