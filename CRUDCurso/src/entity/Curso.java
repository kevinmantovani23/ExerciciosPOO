package entity;

public class Curso {
	
	private int id;
	private String nome;
	private int cod;
	private String nomeCoord;
	private int qntAluno;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getCod() {
		return cod;
	}
	public void setCod(int cod) {
		this.cod = cod;
	}
	public String getNomeCoord() {
		return nomeCoord;
	}
	public void setNomeCoord(String nomeCoord) {
		this.nomeCoord = nomeCoord;
	}
	public int getQntAluno() {
		return qntAluno;
	}
	public void setQntAluno(int qntAluno) {
		this.qntAluno = qntAluno;
	}
	
	
	
	
}
